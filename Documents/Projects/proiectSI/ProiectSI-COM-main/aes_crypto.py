# Round constants for key expansion
_RCON = [
    0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36,
    0x6c,0xd8,0xab,0x4d,0x9a,0x2f,0x5e,0xbc,0x63,0xc6,
    0x97,0x35,0x6a,0xd4,0xb3,0x7d,0xfa,0xef,0xc5,0x91,
]


def _gmul(a, b):
    """Multiply a and b in GF(2^8) with irreducible polynomial x^8+x^4+x^3+x+1."""
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi = a & 0x80
        a = (a << 1) & 0xff
        if hi:
            a ^= 0x1b
        b >>= 1
    return p


def _bytes_to_state(block):
    state = [[0] * 4 for _ in range(4)]
    for r in range(4):
        for c in range(4):
            state[r][c] = block[r + 4 * c]
    return state


def _state_to_bytes(state):
    result = []
    for c in range(4):
        for r in range(4):
            result.append(state[r][c])
    return bytes(result)


def _add_round_key(state, rk):
    return [[state[r][c] ^ rk[r][c] for c in range(4)] for r in range(4)]


def _sub_bytes(state):
    return [[_SBOX[state[r][c]] for c in range(4)] for r in range(4)]


def _inv_sub_bytes(state):
    return [[_INV_SBOX[state[r][c]] for c in range(4)] for r in range(4)]


def _shift_rows(state):
    return [
        [state[0][0], state[0][1], state[0][2], state[0][3]],
        [state[1][1], state[1][2], state[1][3], state[1][0]],
        [state[2][2], state[2][3], state[2][0], state[2][1]],
        [state[3][3], state[3][0], state[3][1], state[3][2]],
    ]


def _inv_shift_rows(state):
    return [
        [state[0][0], state[0][1], state[0][2], state[0][3]],
        [state[1][3], state[1][0], state[1][1], state[1][2]],
        [state[2][2], state[2][3], state[2][0], state[2][1]],
        [state[3][1], state[3][2], state[3][3], state[3][0]],
    ]


def _mix_columns(state):
    result = [[0] * 4 for _ in range(4)]
    for c in range(4):
        s = [state[r][c] for r in range(4)]
        result[0][c] = _gmul(s[0], 2) ^ _gmul(s[1], 3) ^ s[2]          ^ s[3]
        result[1][c] = s[0]           ^ _gmul(s[1], 2) ^ _gmul(s[2], 3) ^ s[3]
        result[2][c] = s[0]           ^ s[1]           ^ _gmul(s[2], 2) ^ _gmul(s[3], 3)
        result[3][c] = _gmul(s[0], 3) ^ s[1]           ^ s[2]           ^ _gmul(s[3], 2)
    return result


def _inv_mix_columns(state):
    result = [[0] * 4 for _ in range(4)]
    for c in range(4):
        s = [state[r][c] for r in range(4)]
        result[0][c] = _gmul(s[0],0x0e) ^ _gmul(s[1],0x0b) ^ _gmul(s[2],0x0d) ^ _gmul(s[3],0x09)
        result[1][c] = _gmul(s[0],0x09) ^ _gmul(s[1],0x0e) ^ _gmul(s[2],0x0b) ^ _gmul(s[3],0x0d)
        result[2][c] = _gmul(s[0],0x0d) ^ _gmul(s[1],0x09) ^ _gmul(s[2],0x0e) ^ _gmul(s[3],0x0b)
        result[3][c] = _gmul(s[0],0x0b) ^ _gmul(s[1],0x0d) ^ _gmul(s[2],0x09) ^ _gmul(s[3],0x0e)
    return result


def _key_expansion(key):
    """AES-256 key schedule. Returns 15 round keys (4x4 each)."""
    nk, nr = 8, 14
    w = [list(key[4*i:4*i+4]) for i in range(nk)]

    for i in range(nk, 4 * (nr + 1)):
        temp = w[i - 1][:]
        if i % nk == 0:
            temp = [temp[1], temp[2], temp[3], temp[0]]  # RotWord
            temp = [_SBOX[b] for b in temp]              # SubWord
            temp[0] ^= _RCON[i // nk - 1]
        elif i % nk == 4:
            temp = [_SBOX[b] for b in temp]              # SubWord (AES-256 only)
        w.append([a ^ b for a, b in zip(w[i - nk], temp)])

    round_keys = []
    for rnd in range(nr + 1):
        rk = [[0] * 4 for _ in range(4)]
        for c in range(4):
            for r in range(4):
                rk[r][c] = w[rnd * 4 + c][r]
        round_keys.append(rk)
    return round_keys


def _aes_encrypt_block(block, round_keys):
    nr = len(round_keys) - 1
    state = _bytes_to_state(block)
    state = _add_round_key(state, round_keys[0])
    for i in range(1, nr):
        state = _sub_bytes(state)
        state = _shift_rows(state)
        state = _mix_columns(state)
        state = _add_round_key(state, round_keys[i])
    state = _sub_bytes(state)
    state = _shift_rows(state)
    state = _add_round_key(state, round_keys[nr])
    return _state_to_bytes(state)


def _aes_decrypt_block(block, round_keys):
    nr = len(round_keys) - 1
    state = _bytes_to_state(block)
    state = _add_round_key(state, round_keys[nr])
    for i in range(nr - 1, 0, -1):
        state = _inv_shift_rows(state)
        state = _inv_sub_bytes(state)
        state = _add_round_key(state, round_keys[i])
        state = _inv_mix_columns(state)
    state = _inv_shift_rows(state)
    state = _inv_sub_bytes(state)
    state = _add_round_key(state, round_keys[0])
    return _state_to_bytes(state)


def _pkcs7_pad(data):
    pad_len = 16 - (len(data) % 16)
    return data + bytes([pad_len] * pad_len)


def _pkcs7_unpad(data):
    pad_len = data[-1]
    return data[:-pad_len]


def _normalize_key(key):
    """Ensure key is exactly 32 bytes, matching the original PKCS7-based logic."""
    if isinstance(key, str):
        key = key.encode('utf-8')
    key = bytes(key)
    if len(key) == 32:
        return key
    # pad to next multiple of 32 then take first 32 (mirrors original pad(...,32)[:32])
    pad_len = 32 - (len(key) % 32)
    key = key + bytes([pad_len] * pad_len)
    return key[:32]


class AESCrypto:
    def __init__(self, key=None):
        if key is None:
            self.key = os.urandom(32)
        else:
            self.key = _normalize_key(key)
        self._round_keys = _key_expansion(self.key)

    def encrypt(self, plaintext):
        if isinstance(plaintext, str):
            plaintext = plaintext.encode('utf-8')
        data = _pkcs7_pad(plaintext)
        iv = os.urandom(16)
        ciphertext = b''
        prev = iv
        for i in range(0, len(data), 16):
            block = bytes(a ^ b for a, b in zip(data[i:i+16], prev))
            enc = _aes_encrypt_block(block, self._round_keys)
            ciphertext += enc
            prev = enc
        return base64.b64encode(iv + ciphertext).decode('utf-8')

    def decrypt(self, ciphertext):
        raw = base64.b64decode(ciphertext)
        iv, ciphertext_bytes = raw[:16], raw[16:]
        plaintext = b''
        prev = iv
        for i in range(0, len(ciphertext_bytes), 16):
            block = ciphertext_bytes[i:i+16]
            dec = _aes_decrypt_block(block, self._round_keys)
            plaintext += bytes(a ^ b for a, b in zip(dec, prev))
            prev = block
        return _pkcs7_unpad(plaintext).decode('utf-8')

    def get_key(self):
        return self.key
